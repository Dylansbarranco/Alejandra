        // Función para obtener una razón no mostrada aún
        function getNextReason() {
            // Si ya mostramos todas las razones, reiniciamos
            if (shownReasons.length >= reasons.length) {
                shownReasons = [];
            }
            
            // Buscamos una razón que no hayamos mostrado aún
            let randomIndex;
            do {
                randomIndex = Math.floor(Math.random() * reasons.length);
            } while (shownReasons.includes(randomIndex));
            
            // Agregamos esta razón a las ya mostradas
            shownReasons.push(randomIndex);
            
            // Actualizamos el contador
            updateReasonCounter();
            
            return reasons[randomIndex];
        }
        
        // Actualizar contador de razones
        function updateReasonCounter() {
            const counter = document.getElementById('reasonCounter');
            counter.textContent = `Razones mostradas: ${shownReasons.length} de 200`;
        }
        
        // Crear razones flotantes
        function createFloatingReason() {
            const reasonElement = document.createElement('div');
            reasonElement.classList.add('reason');
            
            // Obtener la siguiente razón no repetida
            const nextReason = getNextReason();
            reasonElement.textContent = nextReason;
            
            // Posición aleatoria (evitando superposiciones)
            const section = Math.floor(Math.random() * 4); // Dividimos la pantalla en 4 secciones
            const viewportHeight = window.innerHeight;
            const viewportWidth = window.innerWidth;
            
            // Calculamos posición basada en la sección para distribuir mejor
            let topPosition, leftPosition;
            
            switch(section) {
                case 0: // Arriba izquierda
                    topPosition = Math.random() * (viewportHeight/2 - 100);
                    leftPosition = Math.random() * (viewportWidth/2 - 150);
                    break;
                case 1: // Arriba derecha
                    topPosition = Math.random() * (viewportHeight/2 - 100);
                    leftPosition = viewportWidth/2 + Math.random() * (viewportWidth/2 - 150);
                    break;
                case 2: // Abajo izquierda
                    topPosition = viewportHeight/2 + Math.random() * (viewportHeight/2 - 100);
                    leftPosition = Math.random() * (viewportWidth/2 - 150);
                    break;
                case 3: // Abajo derecha
                    topPosition = viewportHeight/2 + Math.random() * (viewportHeight/2 - 100);
                    leftPosition = viewportWidth/2 + Math.random() * (viewportWidth/2 - 150);
                    break;
            }
            
            reasonElement.style.top = topPosition + 'px';
            reasonElement.style.left = leftPosition + 'px';
            
            document.body.appendChild(reasonElement);
            
            // Eliminar después de la animación
            setTimeout(() => {
                reasonElement.remove();
            }, 10000);
        }
        
        // Crear fuegos artificiales
        document.getElementById('createHearts').addEventListener('click', function() {
            // Limpiar cualquier razón anterior
            document.querySelectorAll('.reason').forEach(el => el.remove());
            
            // Reiniciar el contador si se presiona de nuevo
            if (shownReasons.length >= reasons.length) {
                shownReasons = [];
                updateReasonCounter();
            }
            
            // Crear fuegos artificiales
            for (let i = 0; i < 20; i++) {
                setTimeout(() => {
                    createFirework();
                }, i * 100);
            }
            
            // Crear corazones
            for (let i = 0; i < 15; i++) {
                setTimeout(() => {
                    createHeart();
                }, i * 200);
            }
            
            // Mostrar razones de forma secuencial
            // Mostramos 5 razones a la vez, con un intervalo entre cada grupo
            const showReasonGroup = (startIndex) => {
                for (let i = 0; i < 5 && startIndex + i < reasons.length; i++) {
                    setTimeout(() => {
                        if (shownReasons.length < reasons.length) {
                            createFloatingReason();
                        }
                    }, i * 2000); // Cada razón aparece 2 segundos después de la anterior
                }
                
                // Si aún no hemos mostrado todas, programamos el siguiente grupo
                if (startIndex + 5 < reasons.length && shownReasons.length < reasons.length) {
                    setTimeout(() => {
                        showReasonGroup(startIndex + 5);
                    }, 12000); // Esperamos 12 segundos antes del siguiente grupo
                }
            };
            
            // Comenzamos a mostrar las razones
            showReasonGroup(0);
        });
        
